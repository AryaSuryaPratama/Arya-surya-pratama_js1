let keputusan = "membeli";

switch (keputusan) {
    case "membeli":
        console.log("Saya akan membeli rumah tahun depan.");
        break;
    case "menyewa":
        console.log("Saya akan menyewa rumah tahun depan.");
        break;
    default:
        console.log("Saya masih belum yakin dengan keputusan saya.");
}